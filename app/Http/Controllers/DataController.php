     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\RedirectResponse
     */
    public function store(Request $request)
    {
        $attribute = $request->validate([
            'id' => 'required',
            'key' => 'required',
            'value' => 'required',
        ]);

        Data::create([
            'id' => $attribute['id'],
            'key' => $attribute['key'],
            'value' => $attribute['value'],
        ]);

        session()->flash('flash', 'Thanks you');

        return back();
    }

